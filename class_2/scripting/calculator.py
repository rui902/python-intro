#!/usr/bin/env python3
# coding: utf-8
"""Basic calculator example script
Defines 4 basic math functions for the two given numbers
"""


def add(n1, n2):
    """Adds n2 to n1"""
    return n1 + n2


def sub(n1, n2):
    """Subtracts n2 from n1"""
    return n1 - n2


def mul(n1, n2):
    """Multiplies n2 with n1"""
    return n1 * n2


def div(n1, n2):
    """Divides n2 from n1"""
    return n1 / n2

